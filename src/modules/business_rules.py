"""
Add two numbers, return a number
"""


def addition(a, b):
    return a + b


"""
Subtract two numbers, return a number
"""


def subtraction(a, b):
    return a - b


"""
Multiply two numbers, return a number
"""


def multiply(a, b):
    return a * b


"""
Divide two numbers, return a number
"""


def divide(a, b):
    return a / b
